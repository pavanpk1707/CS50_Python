x = int(input("Enter x: "))
y = int(input("Enter y: "))

print(f"Addition of {x} and {y} is {x+y}")
print(f"Subtraction of {x} and {y} is {x-y}")
print(f"Multiplication of {x} and {y} is {x*y}")
print(f"Division of {x} and {y} is {round(x/y,2)}")
print(f"Modulo of {x} and {y} is {x % y}")
print(f"power of {x} and {y} is {x ** y}")